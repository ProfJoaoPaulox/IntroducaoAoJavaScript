// Uma declaração condicional é um conjunto de comandos que são 
//executados caso uma condição especificada seja verdadeira. O 
//JavaScript suporta duas declarações condicionais: if...else e switch.

// IF e ELSE

/*
if(*Aqui vai a condição...Se isso for verdade então faça...){
    Aqui vão as funções que devem ser executadas
} else{ Else significa caso contrário faça isso .....

}
*/

//EXEMPLO
let x = 5;
if(x<10){
    alert(`${x} é menor que 10`)
} else {
    alert(`${x} é maior ou igual a 10`);
}


